package com.example.logindemo.model;

public class DiseaseStatistics {
        private int diabeticRetinopathy;
        private int glaucoma;
        private int macularDegeneration;
        private int cataract;
        private int hypertensiveRetinopathy;
        private int myopia;
        private int other;

        // getter和setter方法
        public int getDiabeticRetinopathy() {
            return diabeticRetinopathy;
        }

        public void setDiabeticRetinopathy(int diabeticRetinopathy) {
            this.diabeticRetinopathy = diabeticRetinopathy;
        }

        public int getGlaucoma() {
            return glaucoma;
        }

        public void setGlaucoma(int glaucoma) {
            this.glaucoma = glaucoma;
        }

        public int getMacularDegeneration() {
            return macularDegeneration;
        }

        public void setMacularDegeneration(int macularDegeneration) {
            this.macularDegeneration = macularDegeneration;
        }

        public int getCataract() {
            return cataract;
        }

        public void setCataract(int cataract) {
            this.cataract = cataract;
        }

        public int getHypertensiveRetinopathy() {
            return hypertensiveRetinopathy;
        }

        public void setHypertensiveRetinopathy(int hypertensiveRetinopathy) {
            this.hypertensiveRetinopathy = hypertensiveRetinopathy;
        }

        public int getMyopia() {
            return myopia;
        }

        public void setMyopia(int myopia) {
            this.myopia = myopia;
        }

        public int getOther() {
            return other;
        }

        public void setOther(int other) {
            this.other = other;
        }
// 其他属性的getter和setter...
}

